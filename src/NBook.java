    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps;
      //  ResultSet rs;
        String str="INSERT INTO `book`(`Book_id`,`Name`, `Edition`, `Publisher`, `Price`, `Pages`) VALUES (?,?,?,?,?,?)";
        int bid=Integer.parseInt(jTextField3.getText());
        String name=jTextField2.getText();
        int edition=Integer.parseInt((String) jComboBox1.getSelectedItem());
        String publisher=jTextField4.getText();
        int price=Integer.parseInt(jTextField5.getText());
        int pages=Integer.parseInt(jTextField6.getText());
        
        try {
            ps=MyConnection.getConnection().prepareStatement(str);
            ps.setInt(1,bid);
            ps.setString(2,name);
            ps.setInt(3,edition);
            ps.setString(4,publisher);
            ps.setInt(5,price);
            ps.setInt(6,pages);
              if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "New Book Added");
            }
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

