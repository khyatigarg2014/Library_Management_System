    public void Book_id_allot()
    {
            String query="SELECT MAX(`Book_id`)+1 from `book`";
            try{
            PreparedStatement pst1 = MyConnection.getConnection().prepareStatement(query);
            ResultSet rs = pst1.executeQuery();
            String book_id;
            while( rs.next())
            {
                book_id = rs.getString(1);
                if(book_id==null)
                {
                    book_id="1";
                }
                jTextField3.setText(book_id);
            } 
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);
            }
    }
